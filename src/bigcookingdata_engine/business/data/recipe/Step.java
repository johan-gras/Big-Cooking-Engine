package bigcookingdata_engine.business.data.recipe;

public class Step {
	
	private int idStep;
	private int numberStep;
	private String descStep;
	
	public int getIdStep() {
		return idStep;
	}
	public void setIdStep(int idStep) {
		this.idStep = idStep;
	}
	public int getNumberStep() {
		return numberStep;
	}
	public void setNumberStep(int numberStep) {
		this.numberStep = numberStep;
	}
	public String getDescStep() {
		return descStep;
	}
	public void setDescStep(String descStep) {
		this.descStep = descStep;
	}
	

}
